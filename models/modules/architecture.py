class LapSRNet3D(nn.Module):
    def __init__(self, in_nc, upscale=4):
        super(LapSRNet3D, self).__init__()
        self.upscale = upscale
        self.conv_input = nn.Conv3d(in_channels=in_nc, out_channels=64, kernel_size=3, stride=1, padding=1, bias=False)
        self.relu = nn.LeakyReLU(0.2, inplace=True)

        # self.convt_i = []
        # self.convt_r = []
        # self.convt_f = []
        # for i in range(upscale // 2):
        #     self.convt_i.append(nn.ConvTranspose3d(in_channels=in_nc, out_channels=in_nc, kernel_size=4, stride=2, padding=1,
        #                                        bias=False))
        #     self.convt_r.append(nn.Conv3d(in_channels=64, out_channels=in_nc, kernel_size=3, stride=1, padding=1, bias=False))
        #     self.convt_f.append(self.make_layer(B.LapSRConvBlock, dim=3))

        self.convt_I1 = nn.ConvTranspose3d(in_channels=in_nc, out_channels=in_nc, kernel_size=4, stride=2, padding=1,
                                           bias=False)
        self.convt_R1 = nn.Conv3d(in_channels=64, out_channels=in_nc, kernel_size=in_nc, stride=1, padding=1, bias=False)
        self.convt_F1 = self.make_layer(B.LapSRBlock, dim=3)

        self.convt_I2 = nn.ConvTranspose3d(in_channels=in_nc, out_channels=in_nc, kernel_size=4, stride=2, padding=1,
                                           bias=False)
        self.convt_R2 = nn.Conv3d(in_channels=64, out_channels=in_nc, kernel_size=in_nc, stride=1, padding=1, bias=False)
        self.convt_F2 = self.make_layer(B.LapSRBlock, dim=3)

        for m in self.modules():
                # if isinstance(m, nn.Conv3d):
                #     n = m.kernel_size[0] * m.kernel_size[1] * m.kernel_size[2] * m.out_channels
                #     m.weight.data.normal_(0, math.sqrt(2. / n))
                #     if m.bias is not None:
                #         m.bias.data.zero_()
                if isinstance(m, nn.ConvTranspose3d):
                    c1, c2, h, w, c = m.weight.data.size()
                    weight = self._get_upsample_filter3d(h)
                    m.weight.data = weight.view(1, 1, h, w, c).repeat(c1, c2, 1, 1, 1)
                    if m.bias is not None:
                        m.bias.data.zero_()

    def _get_upsample_filter3d(self, size):
        """Make a 3D bilinear kernel suitable for upsampling"""
        factor = (size + 1) // 2
        if size % 2 == 1:
            center = factor - 1
        else:
            center = factor - 0.5
        og = np.ogrid[:size, :size, :size]
        filter = (1 - abs(og[0] - center) / factor) * \
                 (1 - abs(og[1] - center) / factor) * \
                 (1 - abs(og[2] - center) / factor)
        return torch.from_numpy(filter).float()

    def make_layer(self, block, dim=3):
        layers = []
        layers.append(block(dim))
        return nn.Sequential(*layers)

    def forward(self, x):
        out = self.relu(self.conv_input(x))

        convt_F1 = self.convt_F1(out)
        convt_I1 = self.convt_I1(x)
        convt_R1 = self.convt_R1(convt_F1)
        HR_2x = convt_I1 + convt_R1

        convt_F2 = self.convt_F2(convt_F1)
        convt_I2 = self.convt_I2(HR_2x)
        convt_R2 = self.convt_R2(convt_F2)
        HR_4x = convt_I2 + convt_R2

        return HR_2x, HR_4x


class SRResNet(nn.Module):
    def __init__(self, in_nc, out_nc, nf, nb, upscale=4, norm_type='batch', act_type='relu', \
            mode='NAC', res_scale=1, upsample_mode='upconv'):
        super(SRResNet, self).__init__()
        n_upscale = int(math.log(upscale, 2))
        if upscale == 3:
            n_upscale = 1

        fea_conv = B.conv_block(in_nc, nf, kernel_size=3, norm_type=None, act_type=None)
        resnet_blocks = [B.ResNetBlock(nf, nf, nf, norm_type=norm_type, act_type=act_type,\
            mode=mode, res_scale=res_scale) for _ in range(nb)]
        LR_conv = B.conv_block(nf, nf, kernel_size=3, norm_type=norm_type, act_type=None, mode=mode)

        if upsample_mode == 'upconv':
            upsample_block = B.upconv_blcok
        elif upsample_mode == 'pixelshuffle':
            upsample_block = B.pixelshuffle_block
        else:
            raise NotImplementedError('upsample mode [{:s}] is not found'.format(upsample_mode))
        if upscale == 3:
            upsampler = upsample_block(nf, nf, 3, act_type=act_type)
        else:
            upsampler = [upsample_block(nf, nf, act_type=act_type) for _ in range(n_upscale)]
        HR_conv0 = B.conv_block(nf, nf, kernel_size=3, norm_type=None, act_type=act_type)
        HR_conv1 = B.conv_block(nf, out_nc, kernel_size=3, norm_type=None, act_type=None)

        self.model = B.sequential(fea_conv, B.ShortcutBlock(B.sequential(*resnet_blocks, LR_conv)),\
            *upsampler, HR_conv0, HR_conv1)

    def forward(self, x):
        x = self.model(x)
        return x


